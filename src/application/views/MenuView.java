    private final Map<JLabel, Color> originalColors = new HashMap<>();

    private void setupHoverEffect(JLabel label) {
        // Set dan simpan warna awal sebagai putih
        label.setBackground(Color.WHITE);
        originalColors.put(label, Color.WHITE);

        // Bikin label transparan & interaktif
        label.setOpaque(true);
        label.setCursor(new Cursor(Cursor.HAND_CURSOR));

        // Tambahkan listener hover
        label.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseEntered(MouseEvent e) {
                label.setBackground(new Color(70, 130, 180)); // Hover color
                label.setForeground(Color.WHITE); // Biar teks tetap kontras
                label.setFont(label.getFont().deriveFont(Font.BOLD, 14f));
                label.setBorder(BorderFactory.createLineBorder(Color.WHITE, 2));
            }

            @Override
            public void mouseExited(MouseEvent e) {
                label.setBackground(originalColors.get(label)); // Kembali ke putih
                label.setForeground(Color.BLACK); // Teks kembali hitam
                label.setFont(label.getFont().deriveFont(Font.PLAIN, 12f));
                label.setBorder(null);
            }
        });
    }



