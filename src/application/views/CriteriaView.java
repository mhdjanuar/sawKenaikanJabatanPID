    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String name = jTextField1.getText();
    
        // Ambil tipe dari dropdown (JComboBox)
        String type = (String) jComboBox1.getSelectedItem(); // Asumsikan jComboBoxType adalah dropdown

        // Parse nilai bobot dan tangani kemungkinan NumberFormatException
        int bobot = 0;
        try {
            bobot = Integer.parseInt(jTextField3.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harap masukkan angka yang valid untuk Bobot.");
            return; // Keluar dari metode jika input tidak valid
        }

        // Pastikan nama dan tipe tidak kosong
        if (name.isEmpty() || type == null || type.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan Tipe tidak boleh kosong.");
            return;
        }

        // Buat instansi CriteriaModel
        CriteriaModel criteria = new CriteriaModel(1, "Kriteria 1");
        criteria.setName(name);
        criteria.setType(type);
        criteria.setBobot(bobot);

        // Coba buat kriteria dalam database atau sumber data
        int result = criteriaDao.create(criteria);

        // Periksa apakah pembuatan berhasil
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
            getAllData(); // Segarkan atau ambil data yang diperbarui
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan data.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

