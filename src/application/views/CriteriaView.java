    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        // Mendapatkan indeks baris yang dipilih
        int selectedRow = jTable1.getSelectedRow();

        // Memeriksa apakah ada baris yang dipilih
        if (selectedRow != -1) {
            // Mengasumsikan ID berada di kolom pertama (indeks 0) dari tabel
            int id = (int) jTable1.getValueAt(selectedRow, 0);

            // Memanggil metode delete dengan ID yang dipilih
            int result = criteriaDao.delete(id); // Hapus record dengan ID yang dipilih

            // Menampilkan pesan hasil
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                // Opsional, muat ulang data tabel setelah penghapusan
                getAllData(); // Mengasumsikan metode ini memuat ulang data ke dalam tabel
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data.");
            }
        } else {
            // Jika tidak ada baris yang dipilih, tampilkan pesan
            JOptionPane.showMessageDialog(this, "Silakan pilih baris untuk menghapus.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

